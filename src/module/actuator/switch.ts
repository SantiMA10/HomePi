
export interface SwitchButton {
    /**
     * Metodo que cambia el estado del interruptor de on a off automaticamente tras un pocos segundos
     */
    blink() : any;

    /**
     * Método que enciende el interruptor
     */
    on() : any;

    /**
     * Método que apaga el interruptor
     */
    off(): any;
}

